function pick<T>(arr: readonly T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

/** Додає крапку в кінці речення, якщо її немає. */
function ensureEndDot(s: string): string {
  const t = s.trim();
  if (!t) return "";
  return /[.?!]$/.test(t) ? t : t + ".";
}

/** Для вплетення в речення: перша літера мала (наприклад «обставини — отрута в організмі»). */
function embedLowerCase(s: string): string {
  const t = s.trim();
  if (!t) return "";
  return t.charAt(0).toLowerCase() + t.slice(1);
}

/**
 * Генерує одну цілісну детективну сцену з фрагментів.
 * Правила: зв’язний художній текст, без механічного склеювання, без повторів і канцеляризмів.
 * Повертає 1–3 абзаци в стилі уривка з детективного роману.
 */
function buildStoryParagraphs(
  introText: string,
  placeText: string,
  bodyLocationText: string,
  weaponText: string,
  evidenceText: string
): [string, string, string, string] {
  const intro = introText.trim();
  const place = placeText.trim();
  const weapon = weaponText.trim();
  const evidence = evidenceText.trim();

  // Нормалізуємо: один раз використовуємо кожен факт, уніфікуємо крапки
  const introNorm = ensureEndDot(intro);
  const placeNorm = ensureEndDot(place);
  const weaponNorm = ensureEndDot(weapon);
  const evidenceNorm = ensureEndDot(evidence);

  // Варіанти першого абзацу: подія й знахідка (одна сцена, без дублів)
  const discoveryParagraphs = [
    () => {
      // Інтро вже містить хто/де/коли — обгортаємо в атмосферу
      const lead = pick([
        "Того вечора в приміщенні було тихо, робочий день щойно закінчився.",
        "Того вечора там ще приймали відвідувачів, зміна працювала до закриття.",
        "Свідки згадують той вечір уривками — кроки, приглушені голоси.",
      ]);
      return `${lead} ${introNorm}`;
    },
    () => {
      return `${introNorm} Поліцію викликали одразу; сцену зафіксували до прибуття експертів.`;
    },
    () => introNorm,
  ];

  // Другий абзац: місце події й висновки експертизи (одна думка, без "за цими даними працюють експерти")
  const sceneAndExpertParagraphs = [
    () => {
      if (!placeNorm && !weaponNorm) return "";
      const parts: string[] = [];
      if (placeNorm) parts.push(`При огляді зафіксували приміщення: ${placeNorm}`);
      if (weaponNorm) parts.push(`Патологоанатоми та слідчі експерти попередньо встановили обставини смерті — ${embedLowerCase(weaponNorm)}`);
      return parts.join(". ");
    },
    () => {
      if (!weaponNorm) return placeNorm ? `Місце події: ${placeNorm}` : "";
      return `${placeNorm ? `Обстановка на місці — ${placeNorm}. ` : ""}За висновками експертів, ${embedLowerCase(weaponNorm)}`;
    },
    () => {
      const placePart = placeNorm ? `Опис місця зводиться до такого: ${placeNorm}. ` : "";
      const expertPart = weaponNorm ? `Експертиза тіла та предметів із сцени дала попередні відповіді — ${embedLowerCase(weaponNorm)}` : "";
      return (placePart + expertPart).trim();
    },
  ];

  // Третій абзац: речові докази і логічне закриття сцени (без шаблонних фраз)
  const evidenceParagraphs = [
    () => {
      if (!evidenceNorm) return "Показання тих, хто був на місці, зіставляють із знахідками.";
      return `${evidenceNorm} Алібі присутніх перевіряють за цими фактами.`;
    },
    () => {
      if (!evidenceNorm) return "Слідство зіставляє свідчення з тим, що зафіксовано на місці.";
      return `На місці залишились сліди: ${embedLowerCase(evidenceNorm)} Показання та алібі тепер зводять у єдину картину.`;
    },
    () => {
      if (!evidenceNorm) return "";
      return `Крім того, під час огляду знайшли таке: ${embedLowerCase(evidenceNorm)} Це лягло в основу порівняння з показаннями.`;
    },
  ];

  const p1 = pick(discoveryParagraphs)();
  let p2 = pick(sceneAndExpertParagraphs)();
  let p3 = pick(evidenceParagraphs)();

  // Якщо другий абзац вийшов порожнім (немає place і weapon), об'єднуємо логіку в один абзац або залишаємо тільки evidence
  if (!p2 && p3) {
    p2 = p3;
    p3 = "";
  } else if (!p2) {
    p2 = "Показання тих, хто був на місці події, зіставляють із зафіксованими обставинами.";
  }

  if (!p3.trim()) p3 = "";

  return [
    ensureEndDot(p1),
    ensureEndDot(p2),
    ensureEndDot(p3),
    "",
  ];
}

/** Обирає свідчення за статтю персонажа, щоб не перемішувати (чоловік не каже «я була»). */
function pickTestimony(suspect: Suspect, isKiller: boolean): string {
  const isMale = suspect.gender === "male";
  if (isKiller) {
    return pick(isMale ? TESTIMONY_HINTS_MALE : TESTIMONY_HINTS_FEMALE);
  }
  return pick(isMale ? TESTIMONY_RED_HERRING_MALE : TESTIMONY_RED_HERRING_FEMALE);
}

/** Друге свідчення (важкий рівень): вбивця — промах під тиском, невинний — брехня/плутанина. */
function pickTestimonySecond(suspect: Suspect, isKiller: boolean): string {
  const isMale = suspect.gender === "male";
  if (isKiller) {
    return pick(isMale ? TESTIMONY_SLIP_MALE : TESTIMONY_SLIP_FEMALE);
  }
  return pick(isMale ? TESTIMONY_LIE_MALE : TESTIMONY_LIE_FEMALE);
}

function shuffle<T>(array: T[]): T[] {
  const out = [...array];
  for (let i = out.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [out[i], out[j]] = [out[j], out[i]];
  }
  return out;
}

/** Pick one from list; if useHint and list has item with link_job === killerJob, 50% pick that. */
function pickPlaceOrWeaponOrBody<T extends { link_job: string | null }>(
  list: T[],
  killerJob: string,
  useHint: boolean
): T {
  if (list.length === 0) throw new Error("Empty story part list");
  const withHint = list.filter((x) => x.link_job === killerJob);
  const pool =
    useHint && withHint.length > 0 && Math.random() < 0.5 ? withHint : list;
  return pick(pool);
}

/** Evidence must always help: only rows that match the killer. Тип доказу випадковий (не завжди взуття). */
function pickEvidenceForKiller(
  evidenceList: { text: string; hint_type: string; hint_value: string }[],
  killer: Suspect
): string {
  const matches = evidenceList.filter((e) => {
    if (e.hint_type === "foot_size") return e.hint_value === String(killer.foot_size);
    if (e.hint_type === "gender") return e.hint_value === killer.gender;
    if (e.hint_type === "job") return e.hint_value === killer.job;
    if (e.hint_type === "bad_habit") return e.hint_value === killer.bad_habit;
    if (e.hint_type === "hair_color") return e.hint_value === killer.hair_color;
    return false;
  });
  if (matches.length === 0) {
    return evidenceList[0]?.text ?? "На місці знайдено сліди; слідство перевіряє алібі.";
  }
  // Один випадковий тип доказу (іноді взуття, іноді стать, звичка, професія тощо)
  const chosen = [pick(matches)];
  if (matches.length > 1 && Math.random() < 0.4) {
    const other = pick(matches.filter((m) => m !== chosen[0]));
    chosen.push(other);
  }
  return chosen.map((c) => c.text).join(" ");
}

export function buildCaseFromParts(
  parts: StoryParts,
  suspects: Suspect[],
  killerIndex: number,
  difficulty: number
): { case: GeneratedCase; caseSuspects: GeneratedCaseSuspect[] } {
  const killer = suspects[killerIndex];
  const isMale = killer.gender === "male";

  if (
    parts.intros.length === 0 ||
    parts.places.length === 0 ||
    parts.weapons.length === 0 ||
    parts.body_locations.length === 0 ||
    parts.evidence.length === 0
  ) {
    throw new Error("Missing story parts in DB; run seed_story_parts.sql");
  }

  // Одна тема справи: інтро, місце, знаряддя й де знайшли тіло — з одного setting
  const settings = [...new Set(parts.intros.map((i) => i.setting).filter(Boolean))] as string[];
  const chosenSetting = settings.length > 0 ? pick(settings) : null;
  const bySetting = <T extends { setting: string | null }>(list: T[]) =>
    chosenSetting ? list.filter((x) => x.setting === chosenSetting) : list;
  const introsForCase = chosenSetting ? parts.intros.filter((i) => i.setting === chosenSetting) : parts.intros;
  const placesForCase = bySetting(parts.places).length > 0 ? bySetting(parts.places) : parts.places;
  const weaponsForCase = bySetting(parts.weapons).length > 0 ? bySetting(parts.weapons) : parts.weapons;
  const bodyForCase =
    bySetting(parts.body_locations).length > 0 ? bySetting(parts.body_locations) : parts.body_locations;

  const intro = pick(introsForCase);
  const place = pickPlaceOrWeaponOrBody(placesForCase, killer.job, true);
  const weapon = pickPlaceOrWeaponOrBody(weaponsForCase, killer.job, true);
  const bodyLocation = pickPlaceOrWeaponOrBody(bodyForCase, killer.job, true);
  const evidenceDescription = pickEvidenceForKiller(parts.evidence, killer);

  const motive = pick(isMale ? MOTIVES_MALE : MOTIVES_FEMALE);
  const confession = pick(isMale ? CONFESSIONS_MALE : CONFESSIONS_FEMALE);

  const [para1, para2, para3, para4] = buildStoryParagraphs(
    intro.text,
    place.text,
    bodyLocation.text,
    weapon.text,
    evidenceDescription
  );

  const caseData: GeneratedCase = {
    intro_text: para1,
    body_location: para2,
    tool_description: para3,
    evidence_description: para4,
    difficulty,
    killer_id: killer.id,
    motive,
    confession_text: confession,
  };

  const isHard = difficulty === 6;
  const caseSuspects: GeneratedCaseSuspect[] = suspects.map((s, i) => {
    const isKiller = i === killerIndex;
    const out: GeneratedCaseSuspect = {
      suspect_id: s.id,
      is_killer: isKiller,
      testimony_text: pickTestimony(s, isKiller),
    };
    if (isHard) {
      out.testimony_text_2 = pickTestimonySecond(s, isKiller);
    }
    return out;
  });

  return { case: caseData, caseSuspects };
}

/**
 * From a pool of suspects, pick `count` at random and choose one as killer.
 */
export function selectSuspectsAndKiller(
  pool: Suspect[],
  count: number
): { suspects: Suspect[]; killerIndex: number } {
  if (pool.length < count) {
    throw new Error(`Not enough suspects: need ${count}, have ${pool.length}`);
  }
  const shuffled = shuffle(pool);
  const suspects = shuffled.slice(0, count);
  const killerIndex = Math.floor(Math.random() * suspects.length);
  return { suspects, killerIndex };
}
